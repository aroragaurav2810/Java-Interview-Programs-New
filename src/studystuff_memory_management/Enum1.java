package studystuff_memory_management;

public class Enum1 

{

	
	
	public static void main(String[] args) 
	{
		System.out.println(4<<2);
		
		for(int i=0;i<9;i+=2)
		{
			
			for(int k=0;k<5;k++)
			{
				System.out.print(" ");
				
			}
			for(int j=9;j>i;j--)
			{
			
				System.out.print("*");
			}
			
			System.out.println("");
			
		}

	}

}
